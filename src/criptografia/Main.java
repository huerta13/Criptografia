    public void mostrarVentanaCarga(JFrame parentFrame, Runnable despuesDeCarga) {
        JDialog dialogo = new JDialog(parentFrame, "Procesando...", true);
        dialogo.setSize(300, 120);
        dialogo.setLayout(new BorderLayout());
        dialogo.setLocationRelativeTo(parentFrame);

        JLabel mensajeLabel = new JLabel("Iniciando...", SwingConstants.CENTER);
        JProgressBar barra = new JProgressBar();
        barra.setIndeterminate(true); // Carga infinita visual

        dialogo.add(mensajeLabel, BorderLayout.CENTER);
        dialogo.add(barra, BorderLayout.SOUTH);

        // Mostrar el diÃ¡logo en un hilo nuevo para no congelar la interfaz
        new Thread(() -> {
            String[] mensajes = {
                "Encriptando...",
                "Descifrando...",
                "Hackeando NASA...",
                "Destruyendo evidencias...",
                "Cargando virus... ðŸ˜ˆ",
                "Listo para lanzar ðŸš€"
            };

            for (String mensaje : mensajes) {
                mensajeLabel.setText(mensaje);
                try {
                    Thread.sleep(700); // Espera entre cada mensaje
                } catch (InterruptedException ex) {
                    ex.printStackTrace();
                }
            }

            dialogo.dispose(); // Cierra el popup
            SwingUtilities.invokeLater(despuesDeCarga); // Ejecuta lo siguiente
        }).start();

        dialogo.setVisible(true);
    }


    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (Area1.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(jPanel1, "Escribe un mensaje", "Alerta", JOptionPane.WARNING_MESSAGE);
        } else {
            Clave clv = new Clave();
            String clave = clv.getClave();

            mostrarVentanaCarga(frame, () -> {

                String txtT, txtN;
                String resultado;
                String txt = Area1.getText();
                int longitud1 = txt.length();
                String lon1 = longitud1 + "";
                if (Selecc.isSelected()) {
                    txtT = Transposicion.transponer(txt);
                    txtT = Transposicion.numeros_vocales(txtT);
                    resultado = Clave.aplicarClave(txtT, clave);
                } else {
                    String txt2 = Area1.getText();
                    txtN = Clave.quitarClave(txt2, clave);
                    txtN = Transposicion.numeros_vocales(txtN);
                    resultado = Transposicion.transponer(txtN);

                }
                Area2.setText(resultado);
                String txt2 = Area2.getText();
                int longitud2 = txt2.length();
                String lon2 = longitud2 + "";
                L1.setText(lon1);
                L2.setText(lon2);
            });
        }
    }//GEN-LAST:event_jButton1ActionPerformed

